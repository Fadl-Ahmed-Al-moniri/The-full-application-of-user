class Product {
  final String name;
  final String image;
  final String harvestDate;
  final String description;
  final double calories;
  final double price;
  final int rating;

  Product({
    required this.name,
    required this.image,
    required this.harvestDate,
    required this.description,
    required this.calories,
    required this.price,
    required this.rating,
  });
}
